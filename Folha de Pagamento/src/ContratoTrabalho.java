public class ContratoTrabalho {

    double valorHoraNormal;
    double valorHoraExtra;
    Funcionario contratoFuncionario;

    public ContratoTrabalho(double valorHoraNormal, double valorHoraExtra, Funcionario contratoFuncionario) {
        this.valorHoraNormal = valorHoraNormal;
        this.valorHoraExtra = valorHoraExtra;
        this.contratoFuncionario = contratoFuncionario;
    }
}
